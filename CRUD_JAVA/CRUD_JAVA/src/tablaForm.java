    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        if (txtNombre.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "El Nombre del contcacto es obligatorio!");
            txtNombre.requestFocus();
        } else if (txtApellidoPaterno.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "El Apellido paterno del contcacto es obligatorio!");
            txtApellidoPaterno.requestFocus();
        } else if (txtApellidoMaterno.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "El Apellido materno del contcacto es obligatorio!");
            txtApellidoMaterno.requestFocus();
        } else if (txtCorreo.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "El Correo del contcacto es obligatorio!");
            txtCorreo.requestFocus();
        } else if (!validaCorreo(txtCorreo.getText())) {
            JOptionPane.showMessageDialog(null, "El Correo ingresado no es valido!");
            txtCorreo.requestFocus();
        } else {
            String name = txtNombre.getText();
            String pat = txtApellidoPaterno.getText();
            String mat = txtApellidoMaterno.getText();
            String mail = txtCorreo.getText();
            p.nuevaPersona(name, pat, mat, mail);
            bloquearControles();
            updateTabla();
            limpiar();
            bloquearControles();
            btnNuevo.setEnabled(true);
            
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void tblContactosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblContactosMouseClicked
       selectItem();
        fila = tblContactos.rowAtPoint(evt.getPoint());
        if (fila > -1) {
            txtNombre.setText(String.valueOf(tblContactos.getValueAt(fila, 1)));
            txtApellidoPaterno.setText(String.valueOf(tblContactos.getValueAt(fila, 2)));
            txtApellidoMaterno.setText(String.valueOf(tblContactos.getValueAt(fila, 3)));
            txtCorreo.setText(String.valueOf(tblContactos.getValueAt(fila, 4)));
        }


    }//GEN-LAST:event_tblContactosMouseClicked

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int opcion = JOptionPane.showConfirmDialog(null, "¬øEst√°s seguro de que quieres eliminar este contacto?", "Confirmar", JOptionPane.YES_NO_OPTION);
        if (opcion == JOptionPane.YES_OPTION) {
            if (fila > -1) {
                String codigo = String.valueOf(tblContactos.getValueAt(fila, 0));
                p.deletePersona(codigo);
                updateTabla();
                fila = -1;
            }
            limpiar();
            bloquearControles();
        }else{
            updateTabla();
            limpiar();
            bloquearControles();
            
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

